#include "libft.h"

char	**ft_split(char const *s, char c)
{
	char	**sp;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s != '\0')
	{
		sp[i][j] == s;
		if (s == c)
			i++;
		j++;
	}
	sp[i][j] = '\0';
	return (sp);
}
